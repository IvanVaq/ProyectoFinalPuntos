# PROYECTO FINAL
# > Crear una aplicación de dibujo con pygame que:
#
#   - Permita dibujar poniendo puntos.
#   - Que guarde las posiciones de los puntos en una BBDD.
#   - Que permita escoger el color de los puntos ()mínimo tres colores).
#   - Que permita exportar la BBDD a un fichero que pueda ser importado
#       posteriormente con los puntos.
#   - Que permita exportar la BBDD mediente un servicio REST-JSON.
# 
#   - OPCIONAL que podamos seleccionar imágenes concretas a exportar.
# 
# 
# 
#       TIEMPO DE ENTREGA
# 
