#       - Debemos poder borrar elementos de la BBDD.
# 
#     OPCIONAL: Que se puedan actulizar elementos.
# 
#   > TERCER ENTREGABLE: Exportación en formato JSON.
#       - Debemos poder exportar la BBDD a un fichero que pueda ser importado
#           posteriormente con los puntos(recomendable JSON).
# 
# 
#   > CUARTO ENTREGABLE: conexion a API remoto.
#       -Que permita esportar/leer la BBDD medianteun servicio REST-JSON. De 
#           esta manera, se podría conectar la aplicación que dibuja, con una 
#           que almacena (o incluso genera automaticamente) listados de puntos, 
#           con sus posiciones y colores 
